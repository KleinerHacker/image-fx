package org.pcsoft.tools.image_fx.plugins.common.exceptions;

/**
 * Created by pfeifchr on 21.08.2014.
 */
public class ImageFXPluginStructureException extends ImageFXPluginException {

    public ImageFXPluginStructureException() {
    }

    public ImageFXPluginStructureException(String message) {
        super(message);
    }

    public ImageFXPluginStructureException(String message, Throwable cause) {
        super(message, cause);
    }

    public ImageFXPluginStructureException(Throwable cause) {
        super(cause);
    }

    public ImageFXPluginStructureException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
